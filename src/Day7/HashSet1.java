package Day7;


import java.util.HashSet;

public class HashSet1 {
	public static void main(String args[])
	{
		HashSet<Integer> t = new HashSet<>();
		t.add(1);
		t.add(2);
		t.add(3);
		t.add(4);
		t.add(5);
		System.out.println("Orginal Hashset:" +t);
		t.add(6);
		System.out.println("New HashSet" +t);
		
		
		
			
	}

}